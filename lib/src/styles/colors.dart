import 'package:flutter/material.dart';

abstract class AppColor {
  static Color green() {
    return Color(0xFFCEFF42);
  }

  static Color orange() {
    return Color(0xFFB28211);
  }

  static Color darkGrey() {
    return Color(0xFFC0BBB0);
  }

  static Color red() {
    return Color(0xFFE74C3C);
  }
}
